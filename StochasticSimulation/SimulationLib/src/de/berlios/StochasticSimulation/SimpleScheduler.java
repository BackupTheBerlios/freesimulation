/** 
 * A simple scheduler example.
 *
 * @author Peter Biechele
 * @version $Revision: 1.1 $ $Date: 2004/03/20 17:50:27 $
 */
public class SimpleScheduler extends Thread {
	int timeslice;

	public SimpleScheduler(int t) {
		timeslice = t;
		setPriority(Thread.MAX_PRIORITY);
		setDaemon(true);
	}

	public void run() {
		while (true)
			try {
				sleep(timeslice);
			} catch (Exception e) {}
	}
}
